package com.hazerta.pojos;

/**
 *
 * @author Alumno
 */
public class Articulo {
    private int id;
    private String nombre;
    private String descripcion;
    private int pvp;
    private int stock;
    private String foto;
    //private int id_categoria;
    //private int id_proveedor;
    private Categoria categoria;
    private Proveedor proveedor;
    private short novedad;
    private short oferta;
    private int cantidadCarrito;

    public int getCantidadCarrito() {
        return cantidadCarrito;
    }

    public void setCantidadCarrito(int cantidadCarrito) {
        this.cantidadCarrito = cantidadCarrito;
    }
    
    

    
    
    public Categoria getCategoria() {
        return categoria;
    }

    public void setCategoria(Categoria categoria) {
        this.categoria = categoria;
    }

    public Proveedor getProveedor() {
        return proveedor;
    }

    public void setProveedor(Proveedor proveedor) {
        this.proveedor = proveedor;
    }

   

    public Articulo() {
        this.categoria=new Categoria(0,"");
        this.proveedor=new Proveedor(0,"","");
   }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public int getPvp() {
        return pvp;
    }

    public void setPvp(int pvp) {
        this.pvp = pvp;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }

    

    public short getNovedad() {
        return novedad;
    }

    public void setNovedad(short novedad) {
        this.novedad = novedad;
    }

    public short getOferta() {
        return oferta;
    }

    public void setOferta(short oferta) {
        this.oferta = oferta;
    }
    
    
    
}
